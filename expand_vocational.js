const fs = require('fs');
const d = require('./src/data/vocationalSubjects.json');

function getSubGroup(code) {
  if (code.startsWith('20001') || code.startsWith('30001')) return 'สมรรถนะวิชาชีพพื้นฐาน';
  const seq = parseInt(code.split('-')[1]);
  if (seq >= 1000 && seq < 2000) return 'สมรรถนะวิชาชีพพื้นฐาน';
  if (seq >= 9000) return 'เลือกเสรี';
  return 'สมรรถนะวิชาชีพเฉพาะ';
}

// Add subGroup to existing
d.pvch.forEach(s => { s.subGroup = getSubGroup(s.code); });
d.pvs.forEach(s => { s.subGroup = getSubGroup(s.code); });

// Helper to create entry
function mk(code, nameTh, nameEn, credit, vocType, department, subGroup, lo, obj, comp, desc) {
  return { code, nameTh, nameEn, credit, vocType, department, subGroup,
    learningOutcome: lo, objectives: obj, competencies: comp, description: desc, standardRef: "-" };
}

